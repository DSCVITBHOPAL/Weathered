import 'dart:convert';

// To parse this JSON data, do
//
//     final weeklyWeather = weeklyWeatherFromJson(jsonString);
WeeklyWeather weeklyWeatherFromJson(String str) =>
    WeeklyWeather.fromJson(json.decode(str));

String weeklyWeatherToJson(WeeklyWeather data) => json.encode(data.toJson());

class WeeklyWeather {
  final String cod;
  final int message;
  final int cnt;
  final List<WeatherList> list;
  final City city;

  WeeklyWeather({
    required this.cod,
    required this.message,
    required this.cnt,
    required this.list,
    required this.city,
  });

  factory WeeklyWeather.fromJson(Map<String, dynamic> json) => WeeklyWeather(
        cod: json["cod"],
        message: json["message"],
        cnt: json["cnt"],
        list: List<WeatherList>.from(
            json["list"].map((x) => WeatherList.fromJson(x))),
        city: City.fromJson(json["city"]),
      );

  Map<String, dynamic> toJson() => {
        "cod": cod,
        "message": message,
        "cnt": cnt,
        "list": List<dynamic>.from(list.map((x) => x.toJson())),
        "city": city.toJson(),
      };
}

class WeatherList {
  final int dt;
  final Main main;
  final List<Weather> weather;
  final Clouds clouds;
  final Wind wind;
  final num visibility;
  final num pop;
  final Sys sys;
  final String dtTxt;

  WeatherList({
    required this.dt,
    required this.main,
    required this.weather,
    required this.clouds,
    required this.wind,
    required this.visibility,
    required this.pop,
    required this.sys,
    required this.dtTxt,
  });

  factory WeatherList.fromJson(Map<String, dynamic> json) => WeatherList(
        dt: json["dt"],
        main: Main.fromJson(json["main"]),
        weather:
            List<Weather>.from(json["weather"].map((x) => Weather.fromJson(x))),
        clouds: Clouds.fromJson(json["clouds"]),
        wind: Wind.fromJson(json["wind"]),
        visibility: json["visibility"],
        pop: json["pop"],
        sys: Sys.fromJson(json["sys"]),
        dtTxt: json["dt_txt"],
      );

  Map<String, dynamic> toJson() => {
        "dt": dt,
        "main": main.toJson(),
        "weather": List<dynamic>.from(weather.map((x) => x.toJson())),
        "clouds": clouds.toJson(),
        "wind": wind.toJson(),
        "visibility": visibility,
        "pop": pop,
        "sys": sys.toJson(),
        "dt_txt": dtTxt,
      };
}

